	/**
	 * 获取字符串在指定索引处汉字拼音的首字母
	 * @param ch
	 * @param index
	 * @return
	 */
	public static String getFirstCh(String ch, int index) {
		String ret = ch;
		if(!TextUtils.isEmpty(ch)) {
			if(index < ch.length()){
				String s = ch.substring(index, index+1).toLowerCase();
				String quanpin= map.get(s);
				if(quanpin != null){
					ret = quanpin.substring(0, 1);
				}
				else {
					ret = s;
				}
			}
		}
		return ret;
	}

	/**
	 * 获取字符串在指定索引处汉字的全拼
	 * @param ch 字符串
	 * @param index 指定的索引
	 * @return
	 */
	public static String getFullPinyin(String ch, int index) {
		String ret = ch;
		if(!TextUtils.isEmpty(ch)) {
			if(index < ch.length()){
				String s = ch.substring(index, index+1).toLowerCase();
				String quanpin= map.get(s);
				if(quanpin != null){
					ret = quanpin;
				}
				else {
					ret = s;
				}
			}
		}
		return ret;
	}

	/**
	 * 获取字符串从索引处后面的一段汉字的全拼
	 * @param ch 字符串
	 * @param index 开始的索引
	 * @param length 获取的长度
	 * @return
	 */
	public static String getFullPinyin(String ch, int index, int length) {
		String ret = "";
		if(!TextUtils.isEmpty(ch) && length <= ch.length()) {
			for (int i = index; i < length; i++) {
				String s = ch.substring(i, i+1).toLowerCase();
				String quanpin= map.get(s);
				if(quanpin != null){
					ret += quanpin;
				}
				else {
					ret += s;
				}
			}
		}
		return ret;
	}

	/**
	 * 获取字符串所有汉字的全拼
	 * @param ch
	 * @return
	 */
	public static String getFullPinyin(String ch) {
		String ret = "";
		if(!TextUtils.isEmpty(ch)) {
			for (int i = 0; i < ch.length(); i++) {
				String s = ch.substring(i, i+1).toLowerCase();
				String quanpin= map.get(s);
				if(quanpin != null){
					ret += quanpin;
				}
				else {
					ret += s;
				}
			}
		}
		return ret;
	}

	/**
	 * 每字的首字母
	 * @param ch
	 * @return
	 */
	public static String getFirstPinyin(String ch) {
		String ret = "";
		if(!TextUtils.isEmpty(ch)) {
			for (int i = 0; i < ch.length(); i++) {
				ret += getFirstCh(ch, i);
			}
		}
		return ret;
	}

